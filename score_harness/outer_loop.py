def run_gate_from_needs_json(
    needs_json: Path,
    gate_script: Path,
    gate_args: list[str],
) -> dict:
    """
    Run traceability_gate.py using metrics.json from the same build as needs.json.

    In this repo, the docs build (Sphinx + score_metamodel extension) produces
    both needs.json and metrics.json. The gate validates the metrics.json.

    This function locates metrics.json in the same directory as needs.json.
    """
    # Locate metrics.json alongside needs.json
    metrics_json = needs_json.parent / "metrics.json"
    if not metrics_json.exists():
        return {
            "metrics": {"error": f"metrics.json not found at {metrics_json}"},
            "gate_passed": False,
            "gate_stdout": f"Error: metrics.json not found at {metrics_json}",
            "gate_returncode": 1,
        }

    metrics = json.loads(metrics_json.read_text())
    gate_result = subprocess.run(
        [sys.executable, str(gate_script), "--metrics-json", str(metrics_json)]
        + gate_args,
        capture_output=True,
        text=True,
    )

    return {
        "metrics": metrics,
        "gate_passed": gate_result.returncode == 0,
        "gate_stdout": gate_result.stdout,
        "gate_returncode": gate_result.returncode,
    }


def run_gate_from_metrics_json(
    metrics_json: Path,
    gate_script: Path,
    gate_args: list[str],
) -> dict:
    """
    Run traceability_gate.py directly on an existing metrics.json fixture.
    This mode is useful for task specs derived from gate tests and fixtures.
    """
    metrics = json.loads(metrics_json.read_text())
    gate_result = subprocess.run(
        [sys.executable, str(gate_script), "--metrics-json", str(metrics_json)]
        + gate_args,
        capture_output=True,
        text=True,
    )

    return {
        "metrics": metrics,
        "gate_passed": gate_result.returncode == 0,
        "gate_stdout": gate_result.stdout,
        "gate_returncode": gate_result.returncode,
    }


def distill_trace(gate_result: dict, task_spec: dict) -> dict:
    """
    Extract only the fields the proposer needs. Never raw stdout dumps.
    This is the distillation step — deterministic, no LLM.
    """
    metrics = gate_result.get("metrics", {})
    impacted = []

    # Extract broken complies/test references by type from metrics
    for req_type, data in metrics.get("metrics_by_type", {}).items():
        reqs = data.get("requirements", {})
        if reqs.get("with_test_link_pct", 100) < 100:
            impacted.append(
                {
                    "artifact_type": req_type,
                    "impact_class": "direct_recheck",
                    "reason": "missing_test_link",
                }
            )
        if reqs.get("with_code_link_pct", 100) < 100:
            impacted.append(
                {
                    "artifact_type": req_type,
                    "impact_class": "direct_recheck",
                    "reason": "missing_code_link",
                }
            )
        tests = data.get("tests", {})
        broken_references = tests.get("broken_references", [])
        if isinstance(broken_references, int):
            broken_reference_count = broken_references
        else:
            broken_reference_count = len(broken_references)
        if broken_reference_count > 0:
            impacted.append(
                {
                    "artifact_type": req_type,
                    "impact_class": "revision_required",
                    "reason": "broken_test_reference",
                    "count": broken_reference_count,
                    "references": broken_references,
                }
            )

    return {
        "gate_passed": gate_result["gate_passed"],
        "expected_verdict": task_spec.get("expected_verdict", "unknown"),
        "task_mode": "metrics_json"
        if task_spec.get("metrics_json_path")
        else "needs_json",
        "verdict_correct": gate_result["gate_passed"]
        == (task_spec.get("expected_verdict") == "pass"),
        "impacted_elements": impacted,
        "coverage_summary": metrics.get("metrics_by_type", {}),
        "responsible_role": task_spec.get("responsible_role", "pr_creator"),
        "provenance": {
            "execution_timestamp": datetime.now(UTC).isoformat(),
            "python_version": sys.version.split()[0],
            "environment_hash": hashlib.sha256(
                f"{sys.version}{os.environ.get('USER', 'unknown')}".encode()
            ).hexdigest()[:16],
            "gate_script_version": "1.0",
        },
    }


def resolve_task_path(path_value: str, task_spec: dict) -> Path:
    """Resolve a task artifact path robustly across common invocation directories."""
    path = Path(path_value)
    if path.is_absolute():
        return path

    candidates = [Path.cwd() / path]
    task_spec_path = Path(task_spec.get("_task_spec_path", ""))
    if task_spec_path:
        task_dir = task_spec_path.parent
        harness_root = task_dir.parent
        candidates.extend(
            [
                task_dir / path,
                harness_root / path,
            ]
        )
        if path.parts and path.parts[0] == harness_root.name:
            candidates.append(harness_root.parent / path)

    for candidate in candidates:
        if candidate.exists():
            return candidate

    return candidates[0]


def resolve_support_path(path_value: Path) -> Path:
    """Resolve repo support scripts independent of the current working directory."""
    if path_value.is_absolute():
        return path_value

    repo_root = Path(__file__).resolve().parent.parent
    candidates = [Path.cwd() / path_value, repo_root / path_value]
    for candidate in candidates:
        if candidate.exists():
            return candidate
    return repo_root / path_value


